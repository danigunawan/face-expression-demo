    /**
     * Create buttons
     * @param  {string}    id
     * @param  {function}  func
     * @return {DOMElement}
     */
    createButtons(id, func) {
        let button = UTILS.getElementById(id);
        if(button) {
            button.addEventListener('click', (e) => {
                e.preventDefault();

                func();
            });
        }
    }


